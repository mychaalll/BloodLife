        <section class="req">
                        <div class = "info-div-3">
                            <div class="face">
                                <h3>REQUIREMENTS</h3>
                                <p>So who can donate blood? According to World Health Organization(WHO),here are some requirements that people should know before donating their blood.</p>
                            </div>

                            <div class="d11">
                                <h4>Age</h4>
                                <h5>You are aged between 18 and 65.</h5>
                                <p class = "requirement-details">
                                    In some countries national legislation permits 16–17 year-olds to donate provided that they fulfil the physical and hematological criteria required and that appropriate consent is obtained.
                                    <br>In some countries, regular donors over the age of 65 may be accepted at the discretion of the responsible physician. The upper age limit in some countries are 60. 
                                </p>
                                <br>
                                <h4>Weight</h4>
                                <h5>You weigh at least 50 kg.</h5>
                                <p class = "requirement-details">
                                    In some countries, donors of whole blood donations should weigh at least 45 kg to donate 350 ml ± 10%.
                                </p>
                            </div>
                            
                            <div class="d2">
                                <h4>Health</h4>
                                <h5>You must be in good health at the time you donate.</h5>
                                <p class = "requirement-details">
                                    You cannot donate if you have a cold, flu, sore throat, cold sore, stomach bug or any other infection.
                                    <br>If you have recently had a tattoo or body piercing you cannot donate for 6 months from the date of the procedure.  If the body piercing was performed by a registered health professional and any inflammation has settled completely, you can donate blood after 12 hours.
                                    <br>If you have visited the dentist for a minor procedure you must wait 24 hours before donating; for major work wait a month.
                                    <br>
                                </p>
                                <h5>You must not donate blood If you do not meet the minimum haemoglobin level for blood donation:</h5>
                                <p>A test will be administered at the donation site. In many countries, a haemoglobin level of not less than 12.0 g/dl for females and not less than 13.0 g/dl for males as the threshold.</p>
                                <h4>Travel</h4>
                                <h5>Travel to areas where mosquito-borne infections are endemic, e.g. malaria, dengue and Zika virus infections, may result in a temporary deferral.</h5>
                                <p class = "requirement-details">
                                    Many countries also implemented the policy to defer blood donors with a history of travel or residence for defined cumulative exposure periods in specified countries or areas, as a measure to reduce the risk of transmitting variant Creutzfeldt-Jakob Disease (vCJD) by blood transfusion.
                                </p>
                            </div>
                            
                           <div class="d3">
                                <h4>Behaviors</h4>
                                <h5>You must not give blood:</h5>
                                <p class = "requirement-details">
                                    If you engaged in “at risk” sexual activity in the past 12 months
                                </p>
                                <h5>Individuals with behaviours below will be deferred permanently:</h5>
                                <p class = "requirement-details">
                                    Have ever had a positive test for HIV (AIDS virus)
                                    <br>Have ever injected recreational drugs.
                                    <br>In the national blood donor selection guidelines, there are more behavior eligibility criteria. Criteria could be different in different countries.
                                </p>
                                <h4>Pregnancy and breastfeeding</h4>
                                <h5>Following pregnancy, the deferral period should last as many months as the duration of the pregnancy.</h5>
                                <p class = "requirement-details">
                                    It is not advisable to donate blood while breast-feeding. Following childbirth, the deferral period is at least 9 months (as for pregnancy) and until 3 months after your baby is significantly weaned (i.e. getting most of his/her nutrition from solids or bottle feeding).
                                </p>
                           </div>
